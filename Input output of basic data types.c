// Input/output of basic data types
#include<stdio.h>
int main()
{
    // Declaration of basic datatypes variable
    int num1 = 5;
    float num2 = 12.55;
    double num3 = 25.34;
    char ch = 'M';

    // Output basic datatypes
    printf("Integer number is: %d\n", num1);
    printf("Floating number is: %f\n", num2);
    printf("Double number is: %lf\n", num3);
    printf("Character value is: %c\n", ch);

    return 0;
}
